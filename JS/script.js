function validSolution(field){
  for(let i = 0; i < 9; i++){
    // check if we have 0 on field
    if(field[i].includes(0)){
     return false;
    }

    // check duplicates in lines of field
    if(field[i].filter((item, index) => field[i].indexOf(item) !== index).length !== 0){
      return false;
    }

    // check duplicates in columns of field
    let columnArr = [];
    for(let j = 0; j < 9; j++){
      columnArr.push(field[j][i]);
    }
    if(columnArr.filter((item, index) => columnArr.indexOf(item) !== index).length !== 0){
      return false;
    }
  }
  return true;
}

